public class Request {
    String category, issue, req_name, req_phone, req_unit;

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getIssue() {
        return issue;
    }

    public void setIssue(String issue) {
        this.issue = issue;
    }

    public String getReq_name() {
        return req_name;
    }

    public void setReq_name(String req_name) {
        this.req_name = req_name;
    }

    public String getReq_phone() {
        return req_phone;
    }

    public void setReq_phone(String req_phone) {
        this.req_phone = req_phone;
    }

    public String getReq_unit() {
        return req_unit;
    }

    public void setReq_unit(String req_unit) {
        this.req_unit = req_unit;
    }




}
